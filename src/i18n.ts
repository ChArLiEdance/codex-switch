export type I18nKey = keyof typeof messages.en;

export type Translate = (key: I18nKey, variables?: Record<string, string | number>) => string;

export function createTranslator(language: UiLanguage): Translate {
  const dictionary = messages[language] ?? messages.en;
  return (key, variables = {}) => {
    const template = String(dictionary[key] ?? messages.en[key] ?? key);
    return Object.entries(variables).reduce(
      (text, [name, value]) => text.split(`{${name}}`).join(String(value)),
      template
    );
  };
}
